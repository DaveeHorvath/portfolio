        let length = data.length
        let current = 0

        let title = document.getElementById("projecttitle")
        let desc = document.getElementById("projectdesc")
        let gh = document.getElementById("projectgh")
        let pics = document.getElementById("projectpics")
        let prev = 0;

        function render() {
            document.getElementById("" + prev).classList.remove("current");
            document.getElementById("" + current).classList.add("current");
            title.innerHTML = data[current].title
            pics.style.backgroundImage = "url("+ data[current].imgurl+")"
            desc.innerHTML = "<p class=\"projectdesctext\">" + data[current].desc + "</p>"
            gh.innerHTML = "<a class=\"cardlink\" href=\"" + data[current].gh + "\">" + data[current].gh + "</a>"
        }

        function set(id) {
            prev = current
            current = id
            render()
        }

        function left() {
            prev = current
            if (current == 0)
                current = length
            current = (current - 1) % length;
            render()
        }

        function right() {
            prev = current
            current = (current + 1) % length;
            render()
        }
        document.addEventListener("DOMContentLoaded", render)